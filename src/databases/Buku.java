package databases;

public class Buku {
    String id;
    String judul;
    String pengarang;
    String penerbit;
    String tahun;
    
    
    
    public Buku(String id, String judul, String pengarang, String penerbit,String tahun) {
        this.id = id;
        this.judul = judul;
        this.pengarang = pengarang;
        this.penerbit = penerbit;
        this.tahun = tahun;
    }
    
  
}
